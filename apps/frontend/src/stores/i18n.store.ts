interface I18nState {
  language: Language;
  t: (key: string) => string;
  setLanguage: (lang: Language) => void;
}

export const useI18nStore = create<I18nState>()(
  persist(
    (set, get) => ({
      language: 'pt-BR',
      t: (key: string) => translations[get().language]?.[key] || key,
      setLanguage: (lang: Language) => set({ language: lang }),
    }),
    { name: 'ferramenta-i18n' }
  )
);